void* AudioVideoRenderCreateNew(HWND hWnd, CAudioVideoRender::eVideoRenderTargets renderTarget, CAudioVideoRender::eFrequencies frequency, CAudioVideoRender::eSignalSources signalSource, LPCWSTR comPort, LPCWSTR leftImageFilePath, LPCWSTR rightImageFilePath)
{
	CAudioVideoRender* cAudioVideoRender = new CAudioVideoRender(hWnd, renderTarget, frequency, signalSource, comPort, leftImageFilePath, rightImageFilePath);
	return((void*)cAudioVideoRender);
}
void AudioVideoRenderDispose(void* handle)
{
	if (handle == NULL) return;
	CAudioVideoRender* cAudioVideoRender = (CAudioVideoRender*)handle;
	delete cAudioVideoRender;
}
CAudioVideoRender::eAudioVideoRenderErrors AudioVideoRenderVideoRender(void* handle)
{
	if (handle == NULL) return(CAudioVideoRender::eAudioVideoRenderErrors::NullHandle);
	CAudioVideoRender* cAudioVideoRender = (CAudioVideoRender*)handle;
	return(cAudioVideoRender->VideoRender());
}
